# When you read an integer value, use input() and then cast a string to integer using int().
# When you read a floating-point number, you will cast a string to float using float(). 
# Note that if you enter an integer value and then float it, the value becomes a floating point number. 
# You know this because the value is display with a ".0" at the end.

x = int(input("Enter an Integer:"))
print(x)
y = float(input("Enter a Floating Point:"))
print(y)